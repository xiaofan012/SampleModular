    private void initMine(){
        //虚线连接
        localFileTree.putClientProperty("JTree.lineStyle", "Angled");
        
        //设置初始树节点数据
        DefaultMutableTreeNode root = null;
        DefaultMutableTreeNode node = null;
//        root = new DefaultMutableTreeNode( new FileNode( "计算机" ) );
//        FileSystemView fsv = FileSystemView.getFileSystemView();
//        File[] files = fsv.getRoots();
//        for( int i=0; i<files.length; i++ ){
//            node = new DefaultMutableTreeNode( new FileNode( files[i] ) );
//            root.add( node );
//        }
        File rootFile = FileSystemView.getFileSystemView().getRoots()[0];
        root = new DefaultMutableTreeNode( new FileNode( rootFile ) );
        File[] lv1 = rootFile.listFiles();
        for( int i=0; i<lv1.length; i++ ){
            File f = lv1[i];
            if( f.isDirectory() ){
                node = new DefaultMutableTreeNode( new FileNode( f ) );
                root.add( node );
            }
        }
        DefaultTreeModel treeModel = new DefaultTreeModel(root);
        localFileTree.setModel(treeModel);
        
        //设置节点渲染器
        localFileTree.setCellRenderer( new FileTreeCellRenderer() );
    }
    
