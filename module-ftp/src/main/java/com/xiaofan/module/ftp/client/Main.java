    private void localFileTreeValueChanged(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_localFileTreeValueChanged
       DefaultMutableTreeNode node = (DefaultMutableTreeNode) localFileTree.getLastSelectedPathComponent();
       FileNode fileNode = (FileNode)node.getUserObject();
       if( fileNode.getFile().isDirectory() ){
           File[] dirs = fileNode.getFile().listFiles();
           for( int i=0; i<dirs.length; i++ ){
               if( dirs[i].isDirectory() ){
                   node.add( new DefaultMutableTreeNode( new FileNode( dirs[i]) ));
               }
           }
       }
    }//GEN-LAST:event_localFileTreeValueChanged

